namespace Cephei.Libraries.Maui.Camera
{
  /// <summary>
  /// The CameraType enum enumerates the different types of camera. Also includes an Any camera type.
  /// </summary>
  public enum CameraType : byte
  {
    /// <summary>
    /// Specifies any camera type.
    /// </summary>
    Any,
    /// <summary>
    /// Specifies a front camera type.
    /// </summary>
    Front,
    /// <summary>
    /// Specifies a back camera type.
    /// </summary>
    Back
  }
}
